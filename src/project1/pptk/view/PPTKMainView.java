    @Override
    public void onChange(BarangModel model) {
        txtNo.setText(model.getNo() + "");
        txtBarang.setText(model.getNama());
        txtQty.setText(model.getQty() + "");
        txtSatuan.setText(model.getSatuan());
        txtHarga.setText(model.getHarga_satuan() + "");
    }

    @Override
    public void onInsert(Barang barang) {
        tabelModel.add(barang);
    }

    @Override
    public void onUpdate(Barang barang) {
        int index = tabelBarang.getSelectedRow();
        tabelModel.set(index, barang);
    }

    @Override
    public void onDelete() {
        int index = tabelBarang.getSelectedRow();
        tabelModel.remove(index);
    }

    @Override
    public void valueChanged(ListSelectionEvent e) {
        try {
            Barang model = tabelModel.get(tabelBarang.getSelectedRow());
            txtNo2.setText(model.getNo()+"");
            txtBarang2.setText(model.getNama());
            txtQty2.setText(model.getQty() + "");
            txtSatuan2.setText(model.getSatuan());
            txtHarga2.setText(model.getHarga_satuan()+ "");
        } catch (IndexOutOfBoundsException exception) {
        }
    }
    
    public void loadDatabase() throws SQLException, BarangException{
        BarangDao dao = new pptkDatabase().getBarangDao();
        tabelModel.setList(dao.selectAllBarang());
    }
