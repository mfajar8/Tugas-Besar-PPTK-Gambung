    private void btnLoginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnLoginMouseClicked
        // TODO add your handling code here:
        String Username;
        String Password;
        Username = txtUsername.getText();
        Password = txtPassword.getText();

        if ((Username.equals("PPTK")) && (Password.equals("Gambung"))) {
            panelLogin.setVisible(false);
            panelMenu.setVisible(true);
            menuKiri.setVisible(true);
            menuKanan.setVisible(true);

            itemAwal.setVisible(true);
            itemDatabase.setVisible(false);
            itemTambah.setVisible(false);
            itemUnduh.setVisible(false);

            txtUsername.setText("USERNAME");
            txtPassword.setText("PASSWORD");
            
            controller.resetBarang(this);
        } else {
            JOptionPane.showMessageDialog(panelLogin, "Username atau Password salah !");
        }
    }//GEN-LAST:event_btnLoginMouseClicked

